
class Calculator(object):
    def __init__(self):
        pass

    def add(self,input1,input2):
            # print(input1 + input2)
        return input1+input2

    def subtract(self,input1,input2):
            # print(input1 - input2)
        return input1-input2

    def time(self,input1,input2):
            # print(input1 * input2)
        return input1 * input2

    def divide(self,input1,input2):
            # print(input1 / input2)
        return input1/input2
